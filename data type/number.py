#사칙연산,四則演算

h = 3
i = 4
print(h + i) #足し算
print(h * i) #掛け算
print(h / i) #割り算
print(h - i) #引き算
print(h ** i)
print(h % i) #あまり
print(i % h)
print(i / h)
print(i // h) #商
